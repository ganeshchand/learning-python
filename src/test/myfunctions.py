def my_contains(elem, lst):
    """(object,list) --> bool
    :param elem is an Element to search in the List
    :param lst is a List  of objects
    :return True if and only if elem is in the list

    """
    return elem in lst

def my_first(lst):
    """
    :param lst is a List of objects
    :return: first element of the list
    """
    return lst[0]
